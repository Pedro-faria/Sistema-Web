	<div class='row'><div class='col-xs-12 col-sm-12 col-md-12 col-lg-12'><h1 align='center'>Definição</h1></div></div><br>
	O software ERP é baseado na existência de bases de dados comuns que permitem o compartilhamento de informações
	com diversos usuários. Dessa forma departamento já não é “proprietário” de dados ou de sistema informático,
	mas todas as unidades de usuários dispõem de recursos de informação e de um conjunto de ferramentas de software
	de apoio à execução, controle e gestão de um negócio, o que é um diferencial importante dos sistemas ERP.</br><br>
	Pode-se dizer que o ERP é um sistema integrado, que possibilita um fluxo de informações único, contínuo e consistente
	por toda a empresa, sob uma única base de dados. É um instrumento para a melhoria de processos de negócios, como
	a produção, compras ou distribuição, com informações on-line e em tempo real.
	Em suma, o sistema permite visualizar por completo as transações efetuadas pela empresa, desenhando um 
	amplo cenário de seus negócios</br><br>
